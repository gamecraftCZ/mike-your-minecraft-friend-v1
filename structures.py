class Vec2:
    x: int or float = 0
    y: int or float = 0

    def __init__(self, x=0, y=0):
        self.x = x
        self.y = y


class Vec3:
    x: int or float = 0
    y: int or float = 0
    z: int or float = 0

    def __init__(self, x=0, y=0, z=0):
        self.x = x
        self.y = y
        self.z = z
